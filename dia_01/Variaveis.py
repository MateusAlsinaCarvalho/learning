# variaveis em Python se comportam de maneira semelhante a Post Its 
# Exemplo:
#%%
nome = "Matuiuda"
# Isso é uma variável que armazena o nome "Matuiuda"
# quando eu invoco a variável nome, o Python vai procurar o valor que está armazenado nela
# e exibir o valor "Matuiuda"

print(nome)

# %%

soma = 1 + 1 
print(" 1 + 1 = ", soma)

soma = soma * 10 
print("resultado da soma vezes 10 é =", soma )

# cada variável pode armazenar um tipo de dado diferente, isso significa que eu posso 
# armazenar esses dados e usa-los da forma que eu quiser posteriormente 
# quando eu sobrescrever uma variável, o Python vai substituir o valor antigo pelo novo valor
# e o valor antigo será perdido, a não ser que eu tenha guardado em outra variável  
